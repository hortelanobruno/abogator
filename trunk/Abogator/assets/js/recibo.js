function generarDatos(){
    var ret=[sueldo,false];
    var indexmes = meses.indexOf(data.mes[0]);
    sueldo.rem[0][2]=data.sueldo[0];
    sueldo.rem[1][2]=data.sueldo[0]*sueldo.rem[1][1]*data.antiguedad[0];
    sueldo.rem[2][2]=data.presentismo[0]?(sueldo.rem[0][2]+sueldo.rem[1][2])/12:0;
    sueldo.rem.total=total(sueldo.rem);
			
    sueldo.nrem[6][2]=data.presentismo[0]?(sueldo.nrem[0][2]+sueldo.nrem[1][2]+sueldo.nrem[2][2]+sueldo.nrem[3][2]+sueldo.nrem[4][2]+sueldo.nrem[5][2])/12:0;
    sueldo.nrem.total=total(sueldo.nrem);
			
    sueldo.desc[0][2]=sueldo.desc[0][1]*sueldo.rem.total;
    sueldo.desc[1][2]=sueldo.desc[1][1]*sueldo.rem.total;
    sueldo.desc[2][2]=sueldo.desc[2][1]*(sueldo.rem.total+sueldo.nrem.total);
    sueldo.desc[3][2]=sueldo.desc[3][1]*(sueldo.rem.total+sueldo.nrem.total);
    sueldo.desc[4][2]=sueldo.desc[4][1]*(sueldo.rem.total+sueldo.nrem.total);
    if(data.convenio[0] == "Comercio"){
        sueldo.desc[5][2]=(indexmes==0||indexmes==12||indexmes==13)?50:(indexmes==23?100:0);
    }
    //sueldo.desc[6][2]=mes==24&&retro?100:0;
    sueldo.desc.total=total(sueldo.desc);
			
			
    if((data.mes[0].indexOf("Junio") != -1)||(data.mes[0].indexOf("Diciembre") != -1)){
        ret[1]=sac;
                
        sac.rem[0][2]=sueldo.rem.total*0.5;
        sac.rem.total=total(sac.rem);
                
        sac.nrem[0][2]=sueldo.nrem[0][2]*(1+(data.presentismo[0]?1/12:0))*0.5;
        sac.nrem[1][2]=sueldo.nrem[1][2]*(1+(data.presentismo[0]?1/12:0))*0.5;
        sac.nrem[2][2]=sueldo.nrem[2][2]*(1+(data.presentismo[0]?1/12:0))*(indexmes==12?2/12:0.5);
        sac.nrem[3][2]=sueldo.nrem[3][2]*(1+(data.presentismo[0]?1/12:0))*(indexmes==24?2/12:0.5);
        sac.nrem.total=total(sac.nrem);
                
        sac.desc[0][2]=sac.desc[0][1]*sac.rem.total;
        sac.desc[1][2]=sac.desc[1][1]*sac.rem.total;
        sac.desc[2][2]=sac.desc[2][1]*(sac.rem.total+sac.nrem.total);
        sac.desc[3][2]=sac.desc[3][1]*(sac.rem.total+sac.nrem.total);
        sac.desc[4][2]=sac.desc[4][1]*(sac.rem.total+sac.nrem.total);
        sac.desc.total=total(sac.desc); 
    }
    function total(obj){
        var ret=0;
        for(var i=0;i<obj.length;i++){
            ret+=obj[i][2];
        }
        return ret;
    }
	
    return ret;
}
		
function calculateRecibo(){
    cargarDatos();
    var datos = generarDatos();
    var table = document.getElementById('bodyRecibo');
    while (table.hasChildNodes()) {
        table.removeChild(table.lastChild);
    }
    var w = 0;
    var suma=[0,0,0];
    for(var i=0;i<datos.length;i++){
        if(datos[i]){
            w = 0;
            for(var det in datos[i]){
                if(!/obj|title/.test(det)){
                    for(var j=0;j<datos[i][det].length;j++){
                        if(datos[i][det][j]){
                            if(datos[i][det][j][2]){
                                var newdiv = document.createElement('tr');
                                newdiv.innerHTML = '<td>'+datos[i][det][j][0]+'</td>';
                                if(datos[i][det][j][1]){
                                    newdiv.innerHTML = newdiv.innerHTML+'<td>'+(datos[i][det][j][1]*100).toFixed(2)+'%</td>';
                                }else{
                                    newdiv.innerHTML = newdiv.innerHTML+'<td></td>';
                                }
                                    
                                for(var q=0; q<=2 ; q++){
                                    if(q==w){
                                        newdiv.innerHTML = newdiv.innerHTML+ '<td>'+(datos[i][det][j][2]).toFixed(2)+'</td>';
                                    }else{
                                        newdiv.innerHTML = newdiv.innerHTML+ '<td></td>';
                                    }
                                }
                                table.appendChild(newdiv);
                            }
                        }
                    }
                    w++;
                }
            }	
            var totaldiv = document.createElement('tr');
            if(datos.length>1){
                totaldiv.innerHTML = '<td>Subtotal</td><td></td>';
            }else{
                totaldiv.innerHTML = '<td>Total</td><td></td>';
            }
                    
            totaldiv.innerHTML = totaldiv.innerHTML + '<td>'+(datos[i].rem.total).toFixed(2)+'</td>';
            totaldiv.innerHTML = totaldiv.innerHTML + '<td>'+(datos[i].nrem.total).toFixed(2)+'</td>';
            totaldiv.innerHTML = totaldiv.innerHTML + '<td>'+(datos[i].desc.total).toFixed(2)+'</td>';
            table.appendChild(totaldiv);
            suma[0]+=datos[i].rem.total;
            suma[1]+=datos[i].nrem.total;
            suma[2]+=datos[i].desc.total;
        }
    }
    if(datos.length>1){
        var totaldiv = document.createElement('tr');
        totaldiv.innerHTML = '<td>Total</td><td></td>';
        totaldiv.innerHTML = totaldiv.innerHTML + '<td>'+(suma[0]).toFixed(2)+'</td>';
        totaldiv.innerHTML = totaldiv.innerHTML + '<td>'+(suma[1]).toFixed(2)+'</td>';
        totaldiv.innerHTML = totaldiv.innerHTML + '<td>'+(suma[2]).toFixed(2)+'</td>';
        table.appendChild(totaldiv);
    }
    var totalfull = document.createElement('tr');
    totalfull.innerHTML = '<td>Sueldo neto a cobrar</td><td></td><td></td><td></td><td>'+(suma[0]+suma[1]-suma[2]).toFixed(2)+'</td>';
    table.appendChild(totalfull);
}

